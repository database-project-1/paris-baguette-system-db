-- =========================================================
-- Q1. 판매 실적이 우수한 상위 5개 지역_시도
-- =========================================================

SELECT
    j.지역_시도,
    COUNT(v.판매번호) AS 판매건수,
    SUM(v.총판매금액) AS 총매출,
    AVG(v.총판매금액) AS 평균판매금액
FROM 지점 j
JOIN 판매 v ON j.지점명 = v.지점명
GROUP BY j.지역_시도
ORDER BY 총매출 DESC
LIMIT 5;


-- =========================================================
-- Q2. 판매 실적이 우수한 상위 5개 매장
-- =========================================================

SELECT
    v.지점명,
    j.지역_시도,
    COUNT(v.판매번호) AS 판매건수,
    SUM(v.총판매금액) AS 총매출
FROM 판매 v
JOIN 지점 j ON v.지점명 = j.지점명
GROUP BY v.지점명, j.지역_시도
ORDER BY 총매출 DESC
LIMIT 5;



-- =========================================================
-- Q3. 우유 관련 상품과 함께 구매된 상품 상위 3개
-- =========================================================

SELECT
    p2.상품코드 AS 함께구매된상품코드,
    p2.이름 AS 함께구매된상품명,
    COUNT(d2.판매번호) AS 함께구매횟수,
    SUM(d2.수량) AS 함께구매수량
FROM 판매상세 d1
JOIN 상품 p1 ON d1.상품코드 = p1.상품코드
JOIN 판매상세 d2 ON d1.판매번호 = d2.판매번호
JOIN 상품 p2 ON d2.상품코드 = p2.상품코드
WHERE p1.이름 LIKE '%우유%'
  AND p2.이름 NOT LIKE '%우유%'
  AND d1.상품코드 <> d2.상품코드
GROUP BY p2.상품코드, p2.이름
ORDER BY 함께구매횟수 DESC, 함께구매수량 DESC
LIMIT 3;


-- =========================================================
-- Q4. 재고가 부족한 상품 상위 10개
-- =========================================================

SELECT
    h.지점명,
    p.상품코드,
    p.이름 AS 상품명,
    b.브랜드명,
    h.재고수량,
    h.최소재고기준,
    h.최소재고기준 - h.재고수량 AS 부족수량
FROM 보유 h
JOIN 상품 p ON h.상품코드 = p.상품코드
LEFT JOIN 브랜드 b ON p.브랜드코드 = b.브랜드코드
WHERE h.재고수량 < h.최소재고기준
ORDER BY 부족수량 DESC, h.지점명
LIMIT 10;


-- =========================================================
-- Q5. 아직 완료되지 않은 발주 상위 10개(오래된 날짜 기준)
-- =========================================================

SELECT
    o.발주번호,
    o.발주일자,
    o.상태,
    o.지점명,
    s.업체명,
    COUNT(d.항목번호) AS 발주항목수,
    SUM(d.주문수량) AS 총주문수량
FROM 발주 o
JOIN 발주상세 d ON o.발주번호 = d.발주번호
JOIN 공급업체 s ON o.업체코드 = s.업체코드
WHERE o.상태 = '대기'
GROUP BY o.발주번호, o.발주일자, o.상태, o.지점명, s.업체명
ORDER BY o.발주일자, o.발주번호
LIMIT 10;


-- =========================================================
-- Q6. 구매 금액이 높은 회원 상위 5명
-- =========================================================

SELECT
    c.고객ID,
    c.이름 AS 고객명,
    m.이메일,
    COUNT(v.판매번호) AS 구매횟수,
    SUM(v.총판매금액) AS 총구매금액
FROM 회원 m
JOIN 고객 c ON m.고객ID = c.고객ID
JOIN 판매 v ON m.고객ID = v.고객ID
GROUP BY c.고객ID, c.이름, m.이메일
ORDER BY 총구매금액 DESC
LIMIT 5;


-- =========================================================
-- Q7. 라떼가 스무디보다 많이 판매된 매장 목록
-- =========================================================

WITH 매장별음료판매 AS (
    SELECT
        v.지점명,
        SUM(CASE
                WHEN p.이름 LIKE '%라떼' THEN d.수량
                ELSE 0
            END) AS 라떼판매량,
        SUM(CASE
                WHEN p.이름 LIKE '%스무디' THEN d.수량
                ELSE 0
            END) AS 스무디판매량
    FROM 판매 v
    JOIN 판매상세 d ON v.판매번호 = d.판매번호
    JOIN 상품 p ON d.상품코드 = p.상품코드
    WHERE p.이름 LIKE '%라떼'
       OR p.이름 LIKE '%스무디'
    GROUP BY v.지점명
)
SELECT
    지점명,
    라떼판매량,
    스무디판매량
FROM 매장별음료판매
WHERE 라떼판매량 > 스무디판매량
ORDER BY 라떼판매량 DESC, 스무디판매량 ASC;

